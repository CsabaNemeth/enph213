function [] = test_discreteCFT()
%[] = test_discreteCFT()
% Written by: Tristan Fleming
% tests the complex discrete Fourier transform function discreteCFT

%% Pulse

x = linspace(pi, 4*pi, 13);
f_x=sin(2*pi*3*x).*(0.5+0.5*cos(2*pi*x+pi)).^3;

% Actual values, as calculated using the student's function
[cAct] = discreteCFT( f_x );

% Check tolerance

tol = 0.0001;

% Expected values
cExp = [0.0410823797687208 - 0.0586838641655102i,0.132691170340720 + 0.0873688559367857i,-0.0639860655560735 + 0.0397908025475077i,0.0114360085733218 + 0.0286854187625459i,-0.0440754992962445 + 0.123570802340923i,-0.0675607328337141 - 0.00687476744151453i,-0.0162847119864470 + 0.00000000000000i,-0.0675607328337141 + 0.00687476744151453i,-0.0440754992962445 - 0.123570802340923i,0.0114360085733218 - 0.0286854187625459i,-0.0639860655560735 - 0.0397908025475077i,0.132691170340720 - 0.0873688559367857i,0.0410823797687208 + 0.0586838641655102i];
% Check
checkEqual(cAct, cExp, tol, 1);

end
